export function isUndef (v) {
  return v === undefined || v === null
}

export function isDef (v){
  return v !== undefined && v !== null
}
export function isPrimitive (value){
  return (
    typeof value === 'string' ||
    typeof value === 'number' ||
    typeof value === 'symbol' ||
    typeof value === 'boolean'
  )
}

